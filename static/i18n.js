const dictionaries = { en, fr, zh };
let currentLang = localStorage.getItem("lang") || (navigator.language || "en").slice(0, 2);
if (!dictionaries[currentLang]) currentLang = "en";

function _lookup(dict, key) {
  return key.split(".").reduce((o, k) => (o && o[k] !== undefined ? o[k] : undefined), dict);
}

export function t(key, vars) {
  const value = _lookup(dictionaries[currentLang], key) ?? _lookup(dictionaries.en, key) ?? key;
  if (typeof value !== "string" || !vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, k) => (vars[k] ?? `{${k}}`));
}

export function getLang() { return currentLang; }
export function availableLangs() { return Object.keys(dictionaries); }
export function setLang(lang, onChange) {
  if (!dictionaries[lang]) return;
  currentLang = lang;
  localStorage.setItem("lang", lang);
  document.documentElement.setAttribute("lang", lang);
  if (onChange) onChange();
}
